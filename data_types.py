# Program using name and age to demonstrate data types

# Variables
name = "Avni"          # String
age = 20               # Integer

# Boolean check
is_adult = age >= 18   # Boolean

# List of hobbies
hobbies = ["Drawing", "Music", "Travel"]  # List

# Display output
print("Name:", name, "| Type:", type(name))
print("Age:", age, "| Type:", type(age))
print("Is Adult:", is_adult, "| Type:", type(is_adult))
print("Hobbies:", hobbies, "| Type:", type(hobbies))